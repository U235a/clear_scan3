def bwareaopen(im, area):
    '''delete small by area elements '''
    retval, labels, stats, _ = cv2.connectedComponentsWithStats(im)
    idx = np.nonzero(stats[:, 4] < area)[0]
    out = np.isin(labels, idx)
    im[out] = 0
    return im


